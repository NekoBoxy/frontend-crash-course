// Declare a variable called season type is number and show the result of season of string
//  	(1 → ‘Spring’, 2 → ‘Summer’, 3 → ‘Fall’, 4 → ‘Winter’)

let season = 1;
switch (season) {
  case 1:
    console.log("Spring");
    break;
  case 2:
    console.log("Summer");
    break;
  case 3:
    console.log("Fall");
    break;
  case 4:
    console.log("Winter");
    break;
  default:
    console.log("季節只有四種");
}